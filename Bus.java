package com.company;

public class Bus {
private String name;
private int nambbus;
private int nambway;
private String brand;
private int year;
private double mileage;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getNambbus() {
        return nambbus;
    }

    public void setNambbus(int nambbus) {
        this.nambbus = nambbus;
    }

    public int getNambway() {
        return nambway;
    }

    public void setNambway(int nambway) {
        this.nambway = nambway;
    }

    public String getBrand() {
        return brand;
    }

    public void setBrand(String brand) {
        this.brand = brand;
    }

    public int getYear() {
        return year;
    }

    public void setYear(int year) {
        this.year = year;
    }

    public double getMileage() {
        return mileage;
    }

    public void setMileage(double mileage) {
        this.mileage = mileage;
    }
}
